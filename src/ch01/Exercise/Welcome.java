package ch01.Exercise; // 패키지 선언문

public class Welcome { // public으로 선언된 클래스 Welcome 정의 시작
    public static void main(String[] args) { // main() 메소드 정의 시작
        System.out.println("Welcome!!"); // println() 메소드 호출하여 문자열 출력
        System.out.println("자바 세계로 오신 것을 환영합니다."); // println() 메소드 호출하여 문자열 출력
    } // main() 메소드 정의 끝
} // Welcome 클래스 정의 끝
